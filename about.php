        <h2 class="txtabout">How To Use.</h2>
          <ul>
           <li>1. Paste the custom tag we note in the settings of the plugin in the head section of your website.</li>
           <li>2. Pick between NOINDEX, FOLLOW or NOINDEX, NOFOLLOW.</li>
           <li>3. Filter pages by adding a word count. Any page that has less than that word count will populate in the results.<br />
            Note: This plugin counts all of the words on a page, not just the words in the WordPress text editor.</li>
           <li>4. Tick any pages that you want to exclude from the NOINDEX processing.</li>
           <li>5. Click the Copy URLs button and paste the URLs into a document for your records. You can supply your team with a list of pages to add content to.</li>
           <li>6. Process the pages. After the plugin processes, your specified NOINDEX tag will appear in the head section of ALL pages that were less than ______ words on a page (except for the pages you purposefully excluded).</li>
           <li>7. Every time you fix a batch of pages and add more content, reprocess the plugin and the NOINDEX tag will fall off of the pages you have fixed.</li>
          </ul>
